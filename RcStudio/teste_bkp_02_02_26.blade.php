        const carousel = document.getElementById("video-carousel")
        const slides = carousel.children
        const slideGap = 16
        let currentIndex = 0
        let isAnimating = false

        function getSlideWidth() {
            return slides[0].offsetWidth + slideGap
        }

        function scrollToIndex(index) {
            if (isAnimating) return
            isAnimating = true

            const maxIndex = slides.length - 1
            if (index < 0) index = maxIndex
            if (index > maxIndex) index = 0

            currentIndex = index

            carousel.scrollTo({
                left: getSlideWidth() * currentIndex,
                behavior: "smooth",
            })

            setTimeout(() => {
                isAnimating = false
                updateDots()
            }, 450)
        }

        function goToSlide(index) {
            scrollToIndex(index)
        }

        function updateDots() {
            document.querySelectorAll(".dot").forEach((dot, i) => {
                dot.classList.toggle("bg-gray-400", i === currentIndex)
                dot.classList.toggle("bg-gray-300", i !== currentIndex)
            })
        }

        /* ===========================
           DRAG COM MOUSE (REAL)
        ============================ */
        let isDown = false
        let startX
        let scrollLeft

        carousel.addEventListener("mousedown", (e) => {
            isDown = true
            carousel.classList.add("cursor-grabbing")
            startX = e.pageX - carousel.offsetLeft
            scrollLeft = carousel.scrollLeft
        })

        carousel.addEventListener("mouseleave", () => {
            isDown = false
            carousel.classList.remove("cursor-grabbing")
        })

        carousel.addEventListener("mouseup", () => {
            isDown = false
            carousel.classList.remove("cursor-grabbing")

            // snap correto após soltar
            currentIndex = Math.round(carousel.scrollLeft / getSlideWidth())
            scrollToIndex(currentIndex)
        })

        carousel.addEventListener("mousemove", (e) => {
            if (!isDown) return
            e.preventDefault()
            const x = e.pageX - carousel.offsetLeft
            const walk = (x - startX) * 1.4
            carousel.scrollLeft = scrollLeft - walk
        })

        /* ===========================
           SINCRONIZA DOTS NO SCROLL
        ============================ */
        let scrollTimeout
        carousel.addEventListener("scroll", () => {
            if (isAnimating || isDown) return

            clearTimeout(scrollTimeout)
            scrollTimeout = setTimeout(() => {
                currentIndex = Math.round(carousel.scrollLeft / getSlideWidth())
                updateDots()
            }, 120)
        })
